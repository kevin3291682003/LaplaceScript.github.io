	function CalRatio(rat)
	{
		var va=document.getElementById('IconSize').value;
		var Rat1=(((rat)-1)/((80-50)/10));//if 50 =1:1,80=Max:1
		var Rat2=Math.abs((va-50)/10);
		if(va>=50)
			return 1+(Rat1*Rat2);
		if(va<50)
			return 1/(1+(Rat1*Rat2));
	}
